#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*result;
	unsigned int	idx;
	size_t			len;

	idx = 0;
	len = ft_strlen(s);
	result = (char *)malloc(sizeof(char) * (len + 1));
	if (!result)
		return (NULL);
	while (idx < len)
	{
		result[idx] = f(idx, s[idx]);
		idx ++;
	}
	result[idx] = '\0';
	return (result);
}
